<?php
$a = 5;
$b = 3;

// igualdade
if($a == $b){
    echo "Valor A é igual a B <br>";
} 

// diferente
if($a != $b){
    echo "Valor A é diferente a B <br>";
}

// idêntico a
if($a === $b){
    echo "Valor de A é idêntico a B <br>";
}

// não idêntico a
if($a !== $b){
    echo "Valor de A é não idêntico a B <br>";
}

// atribuição
if($a = $b){
    echo "Valor de B é atribuido em A, a = $a <br>";
}